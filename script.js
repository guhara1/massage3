const tabs = document.querySelectorAll(".region-tab");
const subregionList = document.querySelector("#subregion-list");
const dongList = document.querySelector("#dong-list");
const regionTitle = document.querySelector("#region-title");
const regionDescription = document.querySelector("#region-description");
const dongTitle = document.querySelector("#dong-title");
const dongSummary = document.querySelector("#dong-summary");
const navToggle = document.querySelector(".nav-toggle");
const mainNav = document.querySelector("#main-nav");

let currentRegion = "seoul";
let currentArea = "강남";

function renderRegion(regionKey, preferredArea) {
  currentRegion = regionKey;
  const region = regionData[regionKey];
  const areaNames = Object.keys(region.areas);
  currentArea = preferredArea && region.areas[preferredArea] ? preferredArea : areaNames[0];

  regionTitle.textContent = region.title;
  regionDescription.textContent = region.description;

  tabs.forEach((tab) => {
    tab.classList.toggle("is-active", tab.dataset.region === regionKey);
  });

  subregionList.innerHTML = areaNames.map((area) => (
    `<button type="button" class="${area === currentArea ? "is-active" : ""}" data-area="${area}">${area} 출장마사지</button>`
  )).join("");

  renderDongs();
}

function renderDongs() {
  const region = regionData[currentRegion];
  const dongs = region.areas[currentArea];
  dongTitle.textContent = `${currentArea} 출장마사지`;
  dongSummary.textContent = `${currentArea} 지역은 방문 시간, 주차·출입 방식, 코스별 준비사항을 예약 전에 확인하면 더 안정적으로 이용할 수 있습니다.`;
  dongList.innerHTML = dongs.map((dong) => (
    `<a href="#guide" aria-label="${currentArea} ${dong} 출장마사지 안내">${dong} 출장마사지</a>`
  )).join("");

  subregionList.querySelectorAll("button").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.area === currentArea);
  });
}

tabs.forEach((tab) => {
  tab.addEventListener("click", () => renderRegion(tab.dataset.region));
});

subregionList.addEventListener("click", (event) => {
  const button = event.target.closest("button[data-area]");
  if (!button) return;
  currentArea = button.dataset.area;
  renderDongs();
});

document.querySelectorAll("[data-region-link]").forEach((link) => {
  link.addEventListener("click", () => renderRegion(link.dataset.regionLink));
});

navToggle.addEventListener("click", () => {
  const isOpen = mainNav.classList.toggle("is-open");
  navToggle.setAttribute("aria-expanded", String(isOpen));
});

renderRegion(currentRegion, currentArea);
